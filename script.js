    y = tile_size * 1,  // starting y of player
    lx = 0,
    keys = {},
    ground = false,
    clock = 0,
    gy = 0, // gravity
    jf = -7.7, // jump force
    run;

window.addEventListener('keydown', function(e) { keys[e.which] = true; })
window.addEventListener('keyup', function(e) { 
  if(e.which == 37 || e.which == 39) {
    player.className = ''
  }
  keys[e.which] = false;   
})

level_box.style.left = lx + 'px'
player.style.left = x + 'px'
player.style.top = y + 'px'

function updatePlayer() {
  var pl = player.getBoundingClientRect(),
      plc = document.elementFromPoint(pl.x + half_tile,pl.y + half_tile),
      ptm = document.elementFromPoint(pl.x + half_tile,pl.y),
      pml = document.elementFromPoint(pl.x - speed,pl.y + half_tile),
      pmr = document.elementFromPoint(pl.x + tile_size + speed,pl.y + half_tile),
      pbm,
      hit = false
  if(ground) {
    pbm = document.elementFromPoint(pl.x + half_tile,pl.y + pl_h)
    player.classList.remove('jumping')
  } else {
    player.classList.add('jumping')
    pbm = document.elementFromPoint(pl.x + half_tile,pl.y + pl_h + 5)
  }

  if(keys[37]) {
    player.classList.add('moving_left')
    player.setAttribute('dir', 'looking_left') 
  }
  if(keys[37] 
     && x >= speed 
     && !pml.classList.contains('solid')) { 
    if(x >= tile_size * 12 || lx == 0) {
      x = x - speed 
    } else {
      lx = lx + b_speed
    }
  }

  if(keys[39]) { 
    player.classList.add('moving_right')
    player.setAttribute('dir', 'looking_right') 
  }
  if(keys[39] 
     && x <= (l_w * tile_size) - tile_size - speed
     && !pmr.classList.contains('solid')) { 

    if(x <= tile_size * 20 || lx <= -tile_size * (96 - 32) + b_speed) {
      x = x + speed       
    } else {
      lx = lx - b_speed
    }
  }
  if(keys[38] && ground){
    gy = jf
  }  

  if(plc.classList.contains('collect')) {
    plc.classList.remove('collect')
  }

  if(ptm.classList.contains('solid') || y <= 0) {
    gy = 2
  }

  if(gy < 9) {
    gy += g  
  }   
  y += gy

  level_box.style.left = lx + 'px'
  player.style.left = x + 'px'
  player.style.top = y + 'px'

  if(pbm.classList.contains('solid')){ 
    gy = -g
    y = Math.round(y / tile_size) * tile_size
    ground = true
  } else {
    ground = false
  }

  clock++
  timer.innerHTML = (clock / 60).toFixed(2)

  if(document.querySelectorAll('.collect').length == 0) {
    clearInterval(run)
  }
}

start_btn.addEventListener('click', function() {
  start_btn.style.display = 'none'
  run = setInterval(updatePlayer,1000/60)
  })